public class Person {
    private String name;
    private int age;
    private String password;

    public String getname(){
        return name;
    }
    public void setname(String name){
        this.name = name;

    }
    public int getage(){
        return age;
    }
    public void setage(int age){
        this.age = age;
    }
    public void setpassword(String password){
        this.password = password;
    }



    
    
}
